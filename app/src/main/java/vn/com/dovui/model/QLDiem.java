package vn.com.dovui.model;

public class QLDiem {
    public int sothutu;
    public String name;
    public int diem;

    public int getSothutu() {
        return sothutu;
    }

    public void setSothutu(int sothutu) {
        this.sothutu = sothutu;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getDiem() {
        return diem;
    }

    public void setDiem(int diem) {
        this.diem = diem;
    }
}
